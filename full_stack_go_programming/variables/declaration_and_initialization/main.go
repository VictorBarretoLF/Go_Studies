package main

import "fmt"

func main() {
	var1 := 100
	var2 := "Go programming"
	var3 := 3.14

	fmt.Printf("variable1 = %d\n", var1)
	fmt.Printf("The type of variable1 is %T\n", var1)

	fmt.Printf("variable2 = %s\n", var2)
	fmt.Printf("The type of variable2 is %T\n", var2)

	fmt.Printf("variable3 = %f\n", var3)
	fmt.Printf("The type of variable3 is %T\n", var3)
}
